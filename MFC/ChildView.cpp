BOOL CChildView::PreCreateWindow(CREATESTRUCT& cs) 
{
	if (!CWnd::PreCreateWindow(cs))
		return FALSE;

	cs.dwExStyle |= WS_EX_CLIENTEDGE;
	cs.style &= ~WS_BORDER;
	cs.lpszClass = AfxRegisterWndClass(CS_HREDRAW|CS_VREDRAW|CS_DBLCLKS, 
		::LoadCursor(nullptr, IDC_ARROW), reinterpret_cast<HBRUSH>(COLOR_WINDOW+1), nullptr);

	return TRUE;
}

int CChildView::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
    if (CWnd::OnCreate(lpCreateStruct) == -1)
        return -1;

    m_xamlIsland.Create(this);
    winrt::Windows::UI::Xaml::Controls::TextBlock txt1;
    txt1.Text(L"Hello from Xaml");
    winrt::Windows::UI::Xaml::Controls::Button bt1;
    bt1.Content(txt1);
    bt1.Height(50);
    bt1.Width(250);
    m_xamlIsland.Content(bt1);
    m_xamlIsland.SetWindowPos(this, 0, 0, 400, 400, SWP_SHOWWINDOW);
}

