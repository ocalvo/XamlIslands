int CMainFrame::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CFrameWndEx::OnCreate(lpCreateStruct) == -1)
		return -1;

	BOOL bNameValid;

	// create a view to occupy the client area of the frame
	if (!m_wndView.Create(nullptr, nullptr, AFX_WS_DEFAULT_VIEW, CRect(0, 0, 0, 0), this, AFX_IDW_PANE_FIRST, nullptr))
	{
		TRACE0("Failed to create view window\n");
		return -1;
	}

    m_wndRibbonBar.Create(this);
    if (0)
    {
        m_wndRibbonBar.LoadFromResource(IDR_RIBBON);
    }
    else
    {
        DEVICE_SCALE_FACTOR scaleFactor = {};
        winrt::check_hresult(GetScaleFactorForMonitor(MonitorFromWindow(GetSafeHwnd(), 0), &scaleFactor));
        const auto dpi = static_cast<int>(scaleFactor) / 100.0f;

        m_xamlIsland.Create(&m_wndRibbonBar);

        winrt::Windows::UI::Xaml::Controls::TextBlock txt1;
        txt1.Text(L"Hello from Xaml");
        winrt::Windows::UI::Xaml::Controls::Button bt1;
        bt1.Content(txt1);
        bt1.Height(50);
        bt1.Width(250);
        m_xamlIsland.Content(bt1);
        m_xamlIsland.SetWindowPos(NULL, 0, 0, static_cast<int>(bt1.Width() * dpi), static_cast<int>(bt1.Height() * dpi), SWP_SHOWWINDOW);
    }

	if (!m_wndStatusBar.Create(this))
	{
		TRACE0("Failed to create status bar\n");
		return -1;      // fail to create
	}


	CString strTitlePane1;
	CString strTitlePane2;
	bNameValid = strTitlePane1.LoadString(IDS_STATUS_PANE1);
	ASSERT(bNameValid);
	bNameValid = strTitlePane2.LoadString(IDS_STATUS_PANE2);
	ASSERT(bNameValid);
	m_wndStatusBar.AddElement(new CMFCRibbonStatusBarPane(ID_STATUSBAR_PANE1, strTitlePane1, TRUE), strTitlePane1);
	m_wndStatusBar.AddExtendedElement(new CMFCRibbonStatusBarPane(ID_STATUSBAR_PANE2, strTitlePane2, TRUE), strTitlePane2);

	// enable Visual Studio 2005 style docking window behavior
	CDockingManager::SetDockingMode(DT_SMART);
	// enable Visual Studio 2005 style docking window auto-hide behavior
	EnableAutoHidePanes(CBRS_ALIGN_ANY);
	// set the visual manager and style based on persisted value
	OnApplicationLook(theApp.m_nAppLook);

    //RECT rect = {};
    //m_wndView.Create(L"MyView", L"MyView", 0, rect, this, 1);

	return 0;
}

